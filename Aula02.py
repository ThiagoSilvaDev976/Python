#Tipos de Dados
a = "Hello World"	                            #tipo str	
b = 20	                                        #tipo int	
c = 20.5                                        #tipo float	
d = 1j	                                        #tipo complex	
e = ["apple", "banana", "cherry"]	            #tipo list	
f = ("apple", "banana", "cherry")	            #tipo tuple	
g = range(6)	                                #tipo range	
h = {"name" : "John", "age" : 36}	            #tipo dict	
i = {"apple", "banana", "cherry"}	            #tipo set	
j = frozenset({"apple", "banana", "cherry"})	#tipo frozenset	
k = True                                        #tipo bool	
l = b"Hello"	                                #tipo bytes	
m = bytearray(5)	                            #tipo bytearray	
n = memoryview(bytes(5))	                    #tipo memoryview	
o = None	                                    #tipo NoneType

#Tipo str (strings)
x = str("s1") # x será 's1'
y = str(2)    # y será '2'
z = str(3.0)  # z será '3.0'

#Tipo int (integrais):
x = int(1)   # x será 1
y = int(2.8) # y será 2
z = int("3") # z será 3

#Tipo float:
x = float(1)     # x será 1.0
y = float(2.8)   # y será 2.8
z = float("3")   # z será 3.0
w = float("4.2") # w será 4.2

