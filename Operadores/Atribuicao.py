#É possivel adicionar valor a uma variável usando +=
saldo = 500
saldo += 200
print(saldo) #700

#É possivel diminuir valor a uma variável usando -=
saldo = 500
saldo -= 200
print(saldo) #300

#É possivel multiplicar o valor de uma variável usando *=
saldo = 500
saldo *= 2
print(saldo) #1000

#É possivel dividir o valor de uma variável usando /= para divisões exatas, e //= para divisões inteiras
saldo = 500
saldo /= 3
print(saldo) #166.66666666666666
saldo //=3
print(saldo) #166

#É possivel obter o resto de uma variável usando %=
saldo = 500
saldo %= 480
print(saldo) #20

#É possível obter a exponeciação/potenciação de uma variável usando **=
saldo = 80
saldo %= 2
print(saldo) #6400