#Igualdade ==
saldo = 450
saque = 200
print(saldo == saque) #resulta em 'false' pois não saõ iguais

#Diferença !=
saldo = 450
saque = 200
print(saldo != saque) #resulta em 'true' pois são diferentes

#Maior que > / Maior ou igual >=
saldo = 450
saque = 200
print(saldo > saque) #true
print(saldo >= saque) #true

#Menor que < / Menor ou igual <=
saldo = 450
saque = 200
print(saldo > saque) #false
print(saldo >= saque) #false